// generates encode.js, the inverse of decode.js: walks an ESTree object
// and writes a binary AST buffer in the v6 wire format that
// `transfer.deserializeFromBuf` reads back into an `ast.Tree`.
const std = @import("std");
const parser = @import("parser");
const ast = parser.ast;
const rt = @import("transfer");
const meta = @import("estree_meta");

const Writer = std.Io.Writer;

pub fn main(init: std.process.Init) !void {
    const stdout = std.Io.File.stdout();
    var buf: [128 * 1024]u8 = undefined;
    var fw = stdout.writer(init.io, &buf);
    try generate(&fw.interface);
    try fw.flush();
}

fn generate(w: *Writer) !void {
    @setEvalBranchQuota(500_000);
    try writePrologue(w);
    try writeInverseMaps(w);
    try writeRuntime(w);
    try writeIdentifierHelpers(w);
    try writeNodeEncoders(w);
    try writeDispatcher(w);
    try writeAssemble(w);
    try w.writeAll("export { encode };\n");
}

// Prologue + constants

fn writePrologue(w: *Writer) !void {
    try w.print(
        \\// generated by tools/gen_estree_encoder.zig, do not edit
        \\//
        \\// Encodes an ESTree object into the Yuku transfer-format buffer.
        \\const NULL = 0xFFFFFFFF;
        \\const _enc = new TextEncoder();
        \\const NODE_SIZE = {[node_size]d};
        \\const HEADER_SIZE = {[hdr_size]d};
        \\const NODE_FLAGS_OFFSET = {[flags_off]d};
        \\const NODE_FIELD0_OFFSET = {[f0_off]d};
        \\const NODE_HEADER_U32S = {[hdr_u32s]d};
        \\const NODE_SPAN_START_U32 = {[ss_u32]d};
        \\const NODE_SPAN_END_U32 = {[se_u32]d};
        \\const COMMENT_SIZE = {[c_size]d};
        \\const COMMENT_FLAGS_OFFSET = {[c_fl]d};
        \\const COMMENT_VALUE_START_OFFSET = {[c_vs]d};
        \\const COMMENT_VALUE_END_OFFSET = {[c_ve]d};
        \\const FLAG_ATTACH_COMMENTS = {[ac]d};
        \\
    , .{
        .node_size = rt.NODE_SIZE,
        .hdr_size = rt.HEADER_SIZE,
        .flags_off = rt.NODE_FLAGS_OFFSET,
        .f0_off = rt.NODE_FIELD0_OFFSET,
        .hdr_u32s = rt.NODE_HEADER_U32S,
        .ss_u32 = rt.NODE_SPAN_START_U32,
        .se_u32 = rt.NODE_SPAN_END_U32,
        .c_size = rt.COMMENT_SIZE,
        .c_fl = rt.COMMENT_FLAGS_OFFSET,
        .c_vs = rt.COMMENT_VALUE_START_OFFSET,
        .c_ve = rt.COMMENT_VALUE_END_OFFSET,
        .ac = rt.FLAG_ATTACH_COMMENTS,
    });
}

// Inverse lookup tables

fn writeInverseMaps(w: *Writer) !void {
    try writeInverseObject(w, "BINARY_OPS_INV", &meta.BINARY_OPS);
    try writeInverseObject(w, "LOGICAL_OPS_INV", &meta.LOGICAL_OPS);
    try writeInverseObject(w, "UNARY_OPS_INV", &meta.UNARY_OPS);
    try writeInverseObject(w, "UPDATE_OPS_INV", &meta.UPDATE_OPS);
    try writeInverseObject(w, "ASSIGNMENT_OPS_INV", &meta.ASSIGNMENT_OPS);
    try writeInverseObject(w, "VAR_KINDS_INV", &meta.VAR_KINDS);
    try writeInverseObject(w, "PROPERTY_KINDS_INV", &meta.PROPERTY_KINDS);
    try writeInverseObject(w, "METHOD_KINDS_INV", &meta.METHOD_KINDS);
    try writeInverseObject(w, "IMPORT_EXPORT_KINDS_INV", &.{ "value", "type" });
    try writeInverseObject(w, "TS_TYPE_OPERATORS_INV", &.{ "keyof", "unique", "readonly" });
    try writeInverseObject(w, "TS_METHOD_SIGNATURE_KINDS_INV", &.{ "method", "get", "set" });
    try writeInverseObject(w, "TS_MODULE_KINDS_INV", &.{ "namespace", "module" });
    try writeInverseObject(w, "IMPORT_PHASE_INV", &.{ "source", "defer" });

    // Mixed-typed enums need inverse functions, not object maps.
    try w.writeAll(
        \\const ACCESSIBILITY_INV = (v) => v == null ? 0 : v === "public" ? 1 : v === "private" ? 2 : 3;
        \\const TS_MAPPED_OPTIONAL_INV = (v) => v === true ? 1 : v === "+" ? 2 : v === "-" ? 3 : 0;
        \\const TS_MAPPED_READONLY_INV = (v) => v === true ? 1 : v === "+" ? 2 : v === "-" ? 3 : 0;
        \\
    );
}

fn writeInverseObject(w: *Writer, name: []const u8, items: []const []const u8) !void {
    try w.print("const {s} = {{", .{name});
    for (items, 0..) |item, i| {
        if (i > 0) try w.writeAll(", ");
        try w.print("\"{s}\": {d}", .{ item, i });
    }
    try w.writeAll("};\n");
}

// Runtime: growable buffers, alloc, encStr, encArr

fn writeRuntime(w: *Writer) !void {
    try w.writeAll(
        \\function encode(result) {
        \\  const root = result.program;
        \\  const lineStarts = result.lineStarts;
        \\  let nodeCap = 512;
        \\  let nodeAB = new ArrayBuffer(nodeCap * NODE_SIZE);
        \\  let nU8 = new Uint8Array(nodeAB);
        \\  let nU32 = new Uint32Array(nodeAB);
        \\  let nodeCount = 0;
        \\  let extraCap = 512;
        \\  let extras = new Uint32Array(extraCap);
        \\  let extraCount = 0;
        \\  let poolCap = 4096;
        \\  let pool = new Uint8Array(poolCap);
        \\  let poolLen = 0;
        \\  const strMap = new Map();
        \\  function alloc() {
        \\    if (nodeCount >= nodeCap) {
        \\      nodeCap *= 2;
        \\      const nb = new ArrayBuffer(nodeCap * NODE_SIZE);
        \\      new Uint8Array(nb).set(nU8);
        \\      nodeAB = nb; nU8 = new Uint8Array(nb); nU32 = new Uint32Array(nb);
        \\    }
        \\    const idx = nodeCount++;
        \\    const b = (idx * NODE_SIZE) >>> 2;
        \\    nU32[b + NODE_HEADER_U32S + 0] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 1] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 2] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 3] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 4] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 5] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 6] = NULL;
        \\    nU32[b + NODE_HEADER_U32S + 7] = NULL;
        \\    return idx;
        \\  }
        \\  function tagAt(idx, tag) { nU8[idx * NODE_SIZE] = tag; }
        \\  function flagsAt(idx, v) {
        \\    const o = idx * NODE_SIZE + NODE_FLAGS_OFFSET;
        \\    nU8[o] = v & 0xFF; nU8[o + 1] = (v >>> 8) & 0xFF;
        \\  }
        \\  function f0At(idx, v) {
        \\    const o = idx * NODE_SIZE + NODE_FIELD0_OFFSET;
        \\    nU8[o] = v & 0xFF; nU8[o + 1] = (v >>> 8) & 0xFF;
        \\  }
        \\  function slotAt(idx, slot, v) {
        \\    nU32[((idx * NODE_SIZE) >>> 2) + NODE_HEADER_U32S + slot] = v >>> 0;
        \\  }
        \\  function spanAt(idx, s, e) {
        \\    const b = (idx * NODE_SIZE) >>> 2;
        \\    nU32[b + NODE_SPAN_START_U32] = (s >>> 0);
        \\    nU32[b + NODE_SPAN_END_U32] = (e >>> 0);
        \\  }
        \\  function pushExtra(idx) {
        \\    if (extraCount >= extraCap) {
        \\      extraCap *= 2;
        \\      const ne = new Uint32Array(extraCap);
        \\      ne.set(extras); extras = ne;
        \\    }
        \\    extras[extraCount++] = idx >>> 0;
        \\  }
        \\  function encArr(arr, encEach) {
        \\    if (!arr || arr.length === 0) return { start: 0, len: 0 };
        \\    const len = arr.length;
        \\    const ids = new Array(len);
        \\    for (let i = 0; i < len; i++) ids[i] = encEach(arr[i]);
        \\    const start = extraCount;
        \\    for (let i = 0; i < len; i++) pushExtra(ids[i]);
        \\    return { start, len };
        \\  }
        \\  function encArrHoles(arr, encEach) {
        \\    if (!arr || arr.length === 0) return { start: 0, len: 0 };
        \\    const len = arr.length;
        \\    const ids = new Array(len);
        \\    for (let i = 0; i < len; i++) { const e = arr[i]; ids[i] = e == null ? NULL : encEach(e); }
        \\    const start = extraCount;
        \\    for (let i = 0; i < len; i++) pushExtra(ids[i]);
        \\    return { start, len };
        \\  }
        \\  function encStr(s) {
        \\    if (s == null || s === "") return { start: 0, end: 0 };
        \\    const hit = strMap.get(s);
        \\    if (hit !== undefined) return hit;
        \\    const bytes = _enc.encode(s);
        \\    while (poolLen + bytes.length > poolCap) {
        \\      poolCap *= 2;
        \\      const np = new Uint8Array(poolCap);
        \\      np.set(pool); pool = np;
        \\    }
        \\    pool.set(bytes, poolLen);
        \\    const r = { start: poolLen, end: poolLen + bytes.length };
        \\    poolLen += bytes.length;
        \\    strMap.set(s, r);
        \\    return r;
        \\  }
        \\  function asStart(n) { return (n && typeof n.start === "number") ? n.start : 0; }
        \\  function asEnd(n) { return (n && typeof n.end === "number") ? n.end : 0; }
        \\  // called by every enc_* to register the node's `.comments` array
        \\  function recordComments(n, idx) {
        \\    if (idx !== NULL && n && Array.isArray(n.comments) && n.comments.length > 0) {
        \\      commentsByIdx.set(idx, n.comments);
        \\    }
        \\  }
        \\
    );
}

// Contextual Identifier dispatch helpers

fn writeIdentifierHelpers(w: *Writer) !void {
    try w.writeAll(
        \\  function encBindingTarget(n) {
        \\    if (n == null) return NULL;
        \\    if (n.type === "Identifier") return enc_binding_identifier(n);
        \\    return encNode(n);
        \\  }
        \\  function encLabel(n) {
        \\    if (n == null) return NULL;
        \\    return enc_label_identifier(n);
        \\  }
        \\  function encPropertyKey(n) {
        \\    if (n == null) return NULL;
        \\    if (n.type === "Identifier") return enc_identifier_name(n);
        \\    return encNode(n);
        \\  }
        \\
    );
}

// Per-tag encoders

fn writeNodeEncoders(w: *Writer) !void {
    inline for (@typeInfo(ast.NodeData).@"union".fields, 0..) |field, tag| {
        if (comptime skipGeneration(field.name)) continue;
        if (comptime meta.isSpecial(field.name)) {
            try writeSpecialEncoder(w, field.name, tag);
        } else {
            try writeGenericEncoder(w, field.name, tag, field.type);
        }
    }
}

/// Yuku-internal node tags that the JS encoder never produces (so we don't
/// emit a dead `enc_*` function for them).
fn skipGeneration(comptime name: []const u8) bool {
    @setEvalBranchQuota(20_000);
    // `function_body` (tag 5) is interchangeable with `block_statement` (tag 4):
    // both decode to ESTree `BlockStatement` and the printer treats them
    // identically. The encoder always uses `block_statement`.
    return std.mem.eql(u8, name, "function_body");
}

fn writeGenericEncoder(w: *Writer, comptime name: []const u8, comptime tag: usize, comptime T: type) !void {
    try w.print("  function enc_{s}(n) {{\n", .{name});

    if (@typeInfo(T) != .@"struct" or @typeInfo(T).@"struct".fields.len == 0) {
        try w.print(
            \\    const idx = alloc();
            \\    tagAt(idx, {d});
            \\    spanAt(idx, asStart(n), asEnd(n));
            \\    recordComments(n, idx);
            \\    return idx;
            \\  }}
            \\
        , .{tag});
        return;
    }

    // Phase 1: encode children into locals (so realloc during recursion
    // never affects pointers we hold).
    inline for (std.meta.fields(T)) |f| {
        const jsf = comptime meta.estreeField(name, f.name);
        if (f.type == ast.NodeIndex) {
            const role = comptime meta.fieldRole(name, f.name);
            const helper = switch (role) {
                .binding => "encBindingTarget",
                .label => "encLabel",
                .auto => "encNode",
            };
            try w.print("    const c_{s} = n.{s} == null ? NULL : {s}(n.{s});\n", .{ f.name, jsf, helper, jsf });
        } else if (f.type == ast.IndexRange) {
            const fn_name = if (comptime meta.isHoleyArray(name, f.name)) "encArrHoles" else "encArr";
            try w.print("    const c_{s} = {s}(n.{s}, encNode);\n", .{ f.name, fn_name, jsf });
        } else if (f.type == ast.String) {
            try w.print("    const c_{s} = encStr(n.{s});\n", .{ f.name, jsf });
        }
    }

    // Phase 2: allocate + write tag.
    try w.print("    const idx = alloc();\n    tagAt(idx, {d});\n", .{tag});

    // Phase 3: write slots.
    inline for (std.meta.fields(T), 0..) |f, i| {
        const slot = comptime rt.u32SlotForField(T, i);
        const jsf = comptime meta.estreeField(name, f.name);
        if (f.type == ast.NodeIndex) {
            try w.print("    slotAt(idx, {d}, c_{s});\n", .{ slot, f.name });
        } else if (f.type == ast.IndexRange) {
            if (comptime rt.isFirstRange(T, i)) {
                try w.print("    f0At(idx, c_{s}.len);\n    slotAt(idx, {d}, c_{s}.start);\n", .{ f.name, slot, f.name });
            } else {
                try w.print("    slotAt(idx, {d}, c_{s}.start);\n    slotAt(idx, {d}, c_{s}.len);\n", .{ slot, f.name, slot + 1, f.name });
            }
        } else if (f.type == ast.String) {
            try w.print("    slotAt(idx, {d}, c_{s}.start);\n    slotAt(idx, {d}, c_{s}.end);\n", .{ slot, f.name, slot + 1, f.name });
        } else if (f.type == ?ast.Hashbang) {
            try w.print("    if (n.{s} != null) {{ const _hb = encStr(n.{s}.value); slotAt(idx, {d}, _hb.start); slotAt(idx, {d}, _hb.end); }}\n", .{ jsf, jsf, slot, slot + 1 });
        }
    }

    // Phase 4: write flags (bools, enums, ?ImportPhase, ?Hashbang presence).
    const any_flag = comptime blk: {
        for (std.meta.fields(T)) |f| {
            if (isFlagField(f.type)) break :blk true;
        }
        break :blk false;
    };
    if (any_flag) {
        try w.writeAll("    flagsAt(idx, 0");
        inline for (std.meta.fields(T), 0..) |f, i| {
            _ = i;
            const bit = comptime rt.flagBitForField(T, std.meta.fieldIndex(T, f.name).?);
            const jsf = comptime meta.estreeField(name, f.name);
            if (f.type == bool) {
                const mask: u32 = @as(u32, 1) << @intCast(bit);
                try w.print(" | (n.{s} ? {d} : 0)", .{ jsf, mask });
            } else if (comptime isEnumFlag(f.type)) {
                const table_inv = comptime meta.enumTableName(f.type) ++ "_INV";
                const needs_fn = comptime meta.enumNeedsInverseFn(f.type);
                if (needs_fn) {
                    if (bit == 0) {
                        try w.print(" | {s}(n.{s})", .{ table_inv, jsf });
                    } else {
                        try w.print(" | ({s}(n.{s}) << {d})", .{ table_inv, jsf, bit });
                    }
                } else {
                    if (bit == 0) {
                        try w.print(" | ({s}[n.{s}] | 0)", .{ table_inv, jsf });
                    } else {
                        try w.print(" | (({s}[n.{s}] | 0) << {d})", .{ table_inv, jsf, bit });
                    }
                }
            } else if (f.type == ?ast.ImportPhase) {
                const pres_mask: u32 = @as(u32, 1) << @intCast(bit);
                try w.print(" | (n.{s} != null ? {d} | ((IMPORT_PHASE_INV[n.{s}] | 0) << {d}) : 0)", .{ jsf, pres_mask, jsf, bit + 1 });
            } else if (f.type == ?ast.Hashbang) {
                const mask: u32 = @as(u32, 1) << @intCast(bit);
                try w.print(" | (n.{s} != null ? {d} : 0)", .{ jsf, mask });
            }
        }
        try w.writeAll(");\n");
    }

    try w.writeAll("    spanAt(idx, asStart(n), asEnd(n));\n    recordComments(n, idx);\n    return idx;\n  }\n");
}

