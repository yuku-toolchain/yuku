// Dispatcher

fn writeDispatcher(w: *Writer) !void {
    try w.writeAll(
        \\  // encoded node index -> its `.comments` array
        \\  const commentsByIdx = new Map();
        \\  function encNode(n) {
        \\    if (n == null) return NULL;
        \\    switch (n.type) {
        \\      case "Identifier": {
        \\        switch (n.kind) {
        \\          case "binding": return enc_binding_identifier(n);
        \\          case "name": return enc_identifier_name(n);
        \\          case "label": return enc_label_identifier(n);
        \\          default: return enc_identifier_reference(n);
        \\        }
        \\      }
        \\      case "PrivateIdentifier": return enc_private_identifier(n);
        \\      case "Literal": {
        \\        if (n.regex) return enc_regexp_literal(n);
        \\        if (typeof n.bigint === "string") return enc_bigint_literal(n);
        \\        const v = n.value;
        \\        if (v === null) return typeof n.raw === "string" && n.raw !== "null" ? enc_numeric_literal(n) : enc_null_literal(n);
        \\        if (typeof v === "boolean") return enc_boolean_literal(n);
        \\        if (typeof v === "string") return enc_string_literal(n);
        \\        if (typeof v === "number") return enc_numeric_literal(n);
        \\        throw new Error("yuku-codegen: unrecognised Literal value");
        \\      }
        \\      case "FunctionDeclaration": return enc_function(n, 0);
        \\      case "FunctionExpression": return enc_function(n, 1);
        \\      case "TSDeclareFunction": return enc_function(n, 2);
        \\      case "TSEmptyBodyFunctionExpression": return enc_function(n, 3);
        \\      case "ClassDeclaration": return enc_class(n, 0);
        \\      case "ClassExpression": return enc_class(n, 1);
        \\      case "MethodDefinition": return enc_method_definition(n, false);
        \\      case "TSAbstractMethodDefinition": return enc_method_definition(n, true);
        \\      case "PropertyDefinition": return enc_property_definition(n, false, false);
        \\      case "TSAbstractPropertyDefinition": return enc_property_definition(n, false, true);
        \\      case "AccessorProperty": return enc_property_definition(n, true, false);
        \\      case "TSAbstractAccessorProperty": return enc_property_definition(n, true, true);
        \\      case "RestElement": return enc_binding_rest_element(n);
        \\      case "TSModuleDeclaration": return n.global ? enc_ts_global_declaration(n) : enc_ts_module_declaration(n);
        \\      case "ExpressionStatement": return n.directive != null ? enc_directive(n) : enc_expression_statement(n);
        \\
    );
    inline for (@typeInfo(ast.NodeData).@"union".fields) |field| {
        const name = field.name;
        if (comptime skipGeneration(name)) continue;
        if (comptime skipInDispatcher(name)) continue;
        const etype = comptime meta.estreeType(name);
        try w.print("      case \"{s}\": return enc_{s}(n);\n", .{ etype, name });
    }
    try w.writeAll(
        \\      default: throw new Error("yuku-codegen: unsupported ESTree node type: " + n.type);
        \\    }
        \\  }
        \\
    );
}

/// nodes that have no direct ESTree dispatcher entry, either because a
/// hardcoded polymorphism branch above already handles their ESTree type,
/// or because they're only invoked indirectly through parent helpers.
fn skipInDispatcher(comptime name: []const u8) bool {
    @setEvalBranchQuota(20_000);
    const skipped = [_][]const u8{
        // ESTree polymorphism already routes these in the hardcoded prelude.
        "identifier_reference", "binding_identifier",    "identifier_name",
        "label_identifier",     "private_identifier",    "string_literal",
        "numeric_literal",      "bigint_literal",        "boolean_literal",
        "null_literal",         "regexp_literal",        "function",
        "class",                "method_definition",     "property_definition",
        "binding_rest_element", "ts_module_declaration", "ts_global_declaration",
        "expression_statement", "directive",
        // not directly addressable by ESTree (no `type:` string for these).
                    "formal_parameter",
        "formal_parameters",    "binding_property",      "ts_this_parameter",
    };
    for (skipped) |s| if (s.len == name.len and std.mem.eql(u8, s, name)) return true;
    return false;
}

// Final assembly: collect sections into one ArrayBuffer

fn writeAssemble(w: *Writer) !void {
    try w.print(
        \\  const progIdx = encNode(root);
        \\  const POSITION_INV = {{ "before": 0, "after": 1, "inside": 2 }};
        \\  // counting-sort comments by host into a prefix-sum offsets table,
        \\  // then write each comment at its slot in `commentBytes`.
        \\  const attachComments = commentsByIdx.size > 0;
        \\  let commentCount = 0;
        \\  let offsetsBytes = null;
        \\  let commentBytes = null;
        \\  if (attachComments) {{
        \\    for (const arr of commentsByIdx.values()) commentCount += arr.length;
        \\    const offsets = new Uint32Array(nodeCount + 1);
        \\    for (const [idx, arr] of commentsByIdx) offsets[idx] = arr.length;
        \\    let sum = 0;
        \\    for (let i = 0; i < nodeCount; i++) {{
        \\      const c = offsets[i];
        \\      offsets[i] = sum;
        \\      sum += c;
        \\    }}
        \\    offsets[nodeCount] = sum;
        \\    offsetsBytes = offsets.buffer;
        \\    commentBytes = new ArrayBuffer(commentCount * COMMENT_SIZE);
        \\    const cU8 = new Uint8Array(commentBytes);
        \\    const cDV = new DataView(commentBytes);
        \\    const cursor = new Uint32Array(nodeCount);
        \\    for (const [idx, arr] of commentsByIdx) {{
        \\      for (let j = 0; j < arr.length; j++) {{
        \\        const c = arr[j];
        \\        const slot = offsets[idx] + cursor[idx]; cursor[idx]++;
        \\        let f = 0;
        \\        if (c.type === "Block") f |= 1;
        \\        f |= ((POSITION_INV[c.position] ?? 0) & 3) << 1;
        \\        if (c.sameLine) f |= 8;
        \\        const v = encStr(typeof c.value === "string" ? c.value : "");
        \\        const o = slot * COMMENT_SIZE;
        \\        cU8[o + COMMENT_FLAGS_OFFSET] = f;
        \\        cDV.setUint32(o + COMMENT_VALUE_START_OFFSET, v.start, true);
        \\        cDV.setUint32(o + COMMENT_VALUE_END_OFFSET, v.end, true);
        \\      }}
        \\    }}
        \\  }}
        \\  const lineStartsCount = Array.isArray(lineStarts) ? lineStarts.length : 0;
        \\  let lineStartsBytes = null;
        \\  if (lineStartsCount > 0) {{
        \\    lineStartsBytes = new ArrayBuffer(lineStartsCount * 4);
        \\    const lsDV = new DataView(lineStartsBytes);
        \\    for (let i = 0; i < lineStartsCount; i++) lsDV.setUint32(i * 4, lineStarts[i] >>> 0, true);
        \\  }}
        \\  const totalNodeBytes = nodeCount * NODE_SIZE;
        \\  const totalExtraBytes = extraCount * 4;
        \\  const totalOffsetsBytes = attachComments ? (nodeCount + 1) * 4 : 0;
        \\  const totalCommentBytes = commentCount * COMMENT_SIZE;
        \\  const totalLineStartsBytes = lineStartsCount * 4;
        \\  const finalSize = HEADER_SIZE + totalNodeBytes + totalExtraBytes + poolLen + totalOffsetsBytes + totalCommentBytes + totalLineStartsBytes;
        \\  const out = new ArrayBuffer(finalSize);
        \\  const outU8 = new Uint8Array(out);
        \\  const outU32 = new Uint32Array(out, 0, (finalSize >>> 2));
        \\  outU32[{[u_nc]d}] = nodeCount;
        \\  outU32[{[u_ec]d}] = extraCount;
        \\  outU32[{[u_sp]d}] = poolLen;
        \\  outU32[{[u_src]d}] = 0;
        \\  outU32[{[u_cc]d}] = commentCount;
        \\  outU32[{[u_ls]d}] = lineStartsCount;
        \\  outU32[{[u_dc]d}] = 0;
        \\  outU32[{[u_pi]d}] = progIdx;
        \\  outU32[{[u_fl]d}] = attachComments ? FLAG_ATTACH_COMMENTS : 0;
        \\  outU32[{[u_fna]d}] = 0;
        \\  const nodesOff = HEADER_SIZE;
        \\  const extrasOff = nodesOff + totalNodeBytes;
        \\  const poolOff = extrasOff + totalExtraBytes;
        \\  const offsetsOff = poolOff + poolLen;
        \\  const commentsOff = offsetsOff + totalOffsetsBytes;
        \\  const lineStartsOff = commentsOff + totalCommentBytes;
        \\  outU8.set(new Uint8Array(nodeAB, 0, totalNodeBytes), nodesOff);
        \\  outU8.set(new Uint8Array(extras.buffer, 0, totalExtraBytes), extrasOff);
        \\  outU8.set(pool.subarray(0, poolLen), poolOff);
        \\  if (offsetsBytes !== null) outU8.set(new Uint8Array(offsetsBytes), offsetsOff);
        \\  if (commentBytes !== null) outU8.set(new Uint8Array(commentBytes), commentsOff);
        \\  if (lineStartsBytes !== null) outU8.set(new Uint8Array(lineStartsBytes), lineStartsOff);
        \\  return out;
        \\}}
        \\
    , .{
        .u_nc = rt.HDR_NODE_COUNT_U32,
        .u_ec = rt.HDR_EXTRA_COUNT_U32,
        .u_sp = rt.HDR_STRING_POOL_LEN_U32,
        .u_src = rt.HDR_SOURCE_LEN_U32,
        .u_cc = rt.HDR_COMMENT_COUNT_U32,
        .u_ls = rt.HDR_LINE_STARTS_COUNT_U32,
        .u_dc = rt.HDR_DIAG_COUNT_U32,
        .u_pi = rt.HDR_PROGRAM_INDEX_U32,
        .u_fl = rt.HDR_FLAGS_U32,
        .u_fna = rt.HDR_FIRST_NON_ASCII_U32,
    });
}
