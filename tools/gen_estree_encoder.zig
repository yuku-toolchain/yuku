    const eql = std.mem.eql;
    if (comptime eql(u8, name, "program")) try writeSpecialProgram(w, tag) //
    else if (comptime eql(u8, name, "directive")) try writeSpecialDirective(w, tag) //
    else if (comptime eql(u8, name, "string_literal")) try writeSpecialStringLit(w, tag) //
    else if (comptime eql(u8, name, "numeric_literal")) try writeSpecialNumericLit(w, tag) //
    else if (comptime eql(u8, name, "bigint_literal")) try writeSpecialBigIntLit(w, tag) //
    else if (comptime eql(u8, name, "boolean_literal")) try writeSpecialBoolLit(w, tag) //
    else if (comptime eql(u8, name, "null_literal")) try writeSpecialNullLit(w, tag) //
    else if (comptime eql(u8, name, "regexp_literal")) try writeSpecialRegExpLit(w, tag) //
    else if (comptime eql(u8, name, "template_element")) try writeSpecialTemplateElement(w, tag) //
    else if (comptime eql(u8, name, "unary_expression")) try writeSpecialUnaryExpr(w, tag) //
    else if (comptime eql(u8, name, "member_expression")) try writeSpecialMemberExpr(w, tag) //
    else if (comptime eql(u8, name, "function")) try writeSpecialFunction(w, tag) //
    else if (comptime eql(u8, name, "arrow_function_expression")) try writeSpecialArrowFn(w, tag) //
    else if (comptime eql(u8, name, "formal_parameter")) try writeSpecialFormalParameter(w, tag) //
    else if (comptime eql(u8, name, "formal_parameters")) try writeSpecialFormalParameters(w, tag) //
    else if (comptime eql(u8, name, "class")) try writeSpecialClass(w, tag) //
    else if (comptime eql(u8, name, "method_definition")) try writeSpecialMethodDef(w, tag) //
    else if (comptime eql(u8, name, "property_definition")) try writeSpecialPropertyDef(w, tag) //
    else if (comptime eql(u8, name, "object_property")) try writeSpecialObjectProperty(w, tag) //
    else if (comptime eql(u8, name, "binding_property")) try writeSpecialBindingProperty(w, tag) //
    else if (comptime eql(u8, name, "array_pattern")) try writeSpecialArrayPattern(w, tag) //
    else if (comptime eql(u8, name, "object_pattern")) try writeSpecialObjectPattern(w, tag) //
    else if (comptime eql(u8, name, "jsx_text")) try writeSpecialJSXText(w, tag) //
    else if (comptime eql(u8, name, "ts_function_type")) try writeSpecialTSFunctionType(w, tag) //
    else if (comptime eql(u8, name, "ts_constructor_type")) try writeSpecialTSConstructorType(w, tag) //
    else if (comptime eql(u8, name, "ts_method_signature")) try writeSpecialTSMethodSig(w, tag) //
    else if (comptime eql(u8, name, "ts_call_signature_declaration")) try writeSpecialTSCallSig(w, tag) //
    else if (comptime eql(u8, name, "ts_construct_signature_declaration")) try writeSpecialTSConstructSig(w, tag) //
    else if (comptime eql(u8, name, "ts_mapped_type")) try writeSpecialTSMappedType(w, tag) //
    else if (comptime eql(u8, name, "ts_module_declaration")) try writeSpecialTSModuleDecl(w, tag) //
    else if (comptime eql(u8, name, "ts_global_declaration")) try writeSpecialTSGlobalDecl(w, tag) //
    else if (comptime eql(u8, name, "ts_this_parameter")) try writeSpecialTSThisParam(w, tag) //
    else if (comptime eql(u8, name, "ts_property_signature")) try writeSpecialTSPropertySig(w, tag) //
    else if (comptime eql(u8, name, "ts_enum_member")) try writeSpecialTSEnumMember(w, tag) //
    else if (comptime eql(u8, name, "ts_index_signature")) try writeSpecialTSIndexSig(w, tag) //
    else @compileError("missing special encoder for: " ++ name);
}

// helpers used inside generic + special encoders
/// true when the field type contributes flag bits (and only flag bits) to the node.
fn isFlagField(comptime F: type) bool {
    if (F == bool) return true;
    if (F == ?ast.ImportPhase) return true;
    if (F == ?ast.Hashbang) return true;
    if (F == ast.NodeIndex or F == ast.IndexRange or F == ast.String) return false;
    return @typeInfo(F) == .@"enum";
}

/// true when the field is a plain enum (no presence bit), which the
/// generic encoder maps through `<TABLE>_INV`. `ast.NodeIndex` is also
/// an enum at the type level (it's `enum(u32)`), so we exclude it.
fn isEnumFlag(comptime F: type) bool {
    if (F == ast.NodeIndex or F == ast.IndexRange or F == ast.String) return false;
    return @typeInfo(F) == .@"enum";
}

fn slotOf(comptime T: type, comptime field: []const u8) u32 {
    return rt.u32SlotForField(T, std.meta.fieldIndex(T, field) orelse @compileError("no field " ++ field));
}
fn flagBit(comptime T: type, comptime field: []const u8) u32 {
    return rt.flagBitForField(T, std.meta.fieldIndex(T, field) orelse @compileError("no field " ++ field));
}
fn flagMask(comptime T: type, comptime field: []const u8) u32 {
    return @as(u32, 1) << @intCast(flagBit(T, field));
}
fn enumMask(comptime E: type) u32 {
    return (@as(u32, 1) << @intCast(rt.enumBitWidth(E))) - 1;
}

fn writeSpecialStringLit(w: *Writer, comptime tag: usize) !void {
    const sv = comptime slotOf(ast.StringLiteral, "value");
    try w.print(
        \\  function enc_string_literal(n) {{
        \\    const s = encStr(typeof n.value === "string" ? n.value : "");
        \\    const idx = alloc();
        \\    tagAt(idx, {d});
        \\    slotAt(idx, {d}, s.start); slotAt(idx, {d}, s.end);
        \\    spanAt(idx, asStart(n), asEnd(n));
        \\    recordComments(n, idx);
        \\    return idx;
        \\  }}
        \\
    , .{ tag, sv, sv + 1 });
}

fn writeSpecialNumericLit(w: *Writer, comptime tag: usize) !void {
    const sr = comptime slotOf(ast.NumericLiteral, "raw");
    try w.print(
        \\  function enc_numeric_literal(n) {{
        \\    const raw = typeof n.raw === "string" ? n.raw : String(n.value);
        \\    let kind = 0;
        \\    if (raw.length > 1 && raw.charCodeAt(0) === 48) {{
        \\      const c = raw.charCodeAt(1);
        \\      if (c === 120 || c === 88) kind = 1;
        \\      else if (c === 111 || c === 79) kind = 2;
        \\      else if (c === 98 || c === 66) kind = 3;
        \\    }}
        \\    const r = encStr(raw);
        \\    const idx = alloc();
        \\    tagAt(idx, {d});
        \\    flagsAt(idx, kind);
        \\    slotAt(idx, {d}, r.start); slotAt(idx, {d}, r.end);
        \\    spanAt(idx, asStart(n), asEnd(n));
        \\    recordComments(n, idx);
        \\    return idx;
        \\  }}
        \\
    , .{ tag, sr, sr + 1 });
}

fn writeSpecialBigIntLit(w: *Writer, comptime tag: usize) !void {
    const sr = comptime slotOf(ast.BigIntLiteral, "raw");
    try w.print(
        \\  function enc_bigint_literal(n) {{
        \\    const raw = typeof n.bigint === "string" ? n.bigint : String(n.value);
