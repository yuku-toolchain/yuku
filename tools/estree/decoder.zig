            \\
            \\      const r = {{
            \\        type: "ArrowFunctionExpression", start, end,
            \\        id: null, generator: false, async: !!(flags & {d}),
            \\        params: f{d} !== NULL ? fnParams(f{d}) : [],
            \\        body: node(f{d}), expression: !!(flags & {d}),
