fn writeArray(w: *Writer, name: []const u8, items: []const []const u8) !void {
    try w.print("const {s} = [", .{name});
    for (items, 0..) |item, i| {
        if (i > 0) try w.writeAll(", ");
        try w.print("\"{s}\"", .{item});
    }
    try w.writeAll("];\n");
}

fn writeArrayRaw(w: *Writer, name: []const u8, items: []const []const u8) !void {
    try w.print("const {s} = [", .{name});
    for (items, 0..) |item, i| {
        if (i > 0) try w.writeAll(", ");
        try w.writeAll(item);
    }
    try w.writeAll("];\n");
}

