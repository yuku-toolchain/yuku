            \\
            \\      const r = {{
            \\        type: CLASS_TYPES[flags & {d}], start, end,
            \\        decorators: nodeArr(f{d}, f0),
            \\        id: f{d} !== NULL ? node(f{d}) : null,
            \\        superClass: f{d} !== NULL ? node(f{d}) : null,
            \\        body: node(f{d}),
            \\      }};
