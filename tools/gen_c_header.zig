fn writeEnums(w: *Writer) !void {
    try w.writeAll(
        \\// --- enums ---
        \\
        \\
    );
    try writeEnumFromType(w, "YukuNodeType", ast.NodeData, null);
    try writeEnumFromType(w, "YukuSourceType", ast.SourceType, null);
    try writeEnumFromType(w, "YukuLang", ast.Lang, null);
    try writeEnumFromType(w, "YukuSeverity", ast.Severity, null);
    try writeEnumFromType(w, "YukuBinaryOp", ast.BinaryOperator, null);
    try writeEnumFromType(w, "YukuLogicalOp", ast.LogicalOperator, null);
    try writeEnumFromType(w, "YukuUnaryOp", ast.UnaryOperator, null);
    try writeEnumFromType(w, "YukuUpdateOp", ast.UpdateOperator, null);
    try writeEnumFromType(w, "YukuAssignOp", ast.AssignmentOperator, null);
    try writeEnumFromType(w, "YukuVariableKind", ast.VariableKind, null);
    try writeEnumFromType(w, "YukuPropertyKind", ast.PropertyKind, null);
    try writeEnumFromType(w, "YukuMethodKind", ast.MethodDefinitionKind, null);
    try writeEnumFromType(w, "YukuFunctionType", ast.FunctionType, null);
    try writeEnumFromType(w, "YukuClassType", ast.ClassType, null);
    try writeEnumFromType(w, "YukuFormalParamKind", ast.FormalParameterKind, null);
    try writeEnumFromType(w, "YukuImportExportKind", ast.ImportOrExportKind, null);
    try writeEnumFromType(w, "YukuNumericKind", ast.NumericLiteral.Kind, null);
    try writeEnumFromType(w, "YukuCommentType", ast.Comment.Type, null);
    try writeEnumFromType(w, "YukuImportPhase", ast.ImportPhase, null);
}

fn writeEnumFromType(w: *Writer, comptime c_name: []const u8, comptime T: type, comptime prefix: ?[]const u8) !void {
    const info = @typeInfo(T);
    const fields = switch (info) {
        .@"enum" => |e| e.fields,
        .@"union" => |u| u.fields,
        else => @compileError("expected enum or union, got " ++ @typeName(T)),
    };
    try w.print("typedef enum {{\n", .{});
    inline for (fields, 0..) |f, i| {
        const p = if (prefix) |pp| pp else enumToPrefix(c_name);
        try w.print("    YUKU_{s}_{s} = {d},\n", .{ p, f.name, i });
    }
    try w.print("}} {s};\n\n", .{c_name});
}

fn enumToPrefix(comptime name: []const u8) []const u8 {
    const mapping = .{
        .{ "YukuNodeType", "NODE" },
        .{ "YukuSourceType", "SOURCE" },
        .{ "YukuLang", "LANG" },
        .{ "YukuSeverity", "SEV" },
        .{ "YukuBinaryOp", "BINOP" },
        .{ "YukuLogicalOp", "LOGOP" },
        .{ "YukuUnaryOp", "UNOP" },
        .{ "YukuUpdateOp", "UPOP" },
        .{ "YukuAssignOp", "ASSIGNOP" },
        .{ "YukuVariableKind", "VAR" },
        .{ "YukuPropertyKind", "PROP_KIND" },
        .{ "YukuMethodKind", "METHOD" },
        .{ "YukuFunctionType", "FUNC" },
        .{ "YukuClassType", "CLASS" },
        .{ "YukuFormalParamKind", "FPARAM" },
        .{ "YukuImportExportKind", "IMPEXP" },
        .{ "YukuNumericKind", "NUM" },
        .{ "YukuCommentType", "COMMENT" },
        .{ "YukuImportPhase", "PHASE" },
    };
    inline for (mapping) |m| {
        if (comptime std.mem.eql(u8, name, m[0])) return m[1];
    }
    @compileError("no prefix mapping for " ++ name);
}

// ============================================================================
// packed node struct (matches transfer.zig PackedNode layout)
// ============================================================================

fn writePackedNodeStruct(w: *Writer) !void {
    try w.writeAll(
        \\// --- packed node layout (32 bytes, little-endian) ---
        \\
        \\typedef struct {
        \\    uint8_t  tag;         // YukuNodeType
        \\    uint8_t  flags;
        \\    uint16_t field0;     // first IndexRange len (or 0)
        \\    uint32_t field1;     // u32 slot 0
        \\    uint32_t field2;     // u32 slot 1
        \\    uint32_t field3;     // u32 slot 2
        \\    uint32_t field4;     // u32 slot 3
        \\    uint32_t field5;     // u32 slot 4
        \\    uint32_t span_start;
        \\    uint32_t span_end;
        \\} YukuPackedNode;
        \\
        \\
    );
}

// ============================================================================
// inline accessors (zero-overhead buffer reading)
// ============================================================================

fn writeInlineAccessors(w: *Writer) !void {
    try w.writeAll(
        \\// --- inline buffer accessors ---
        \\// all functions are static inline — zero overhead after yuku_parse()
        \\
        \\// header accessors
        \\static inline uint32_t yuku_header_magic(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_version(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 4, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_node_count(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 8, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_extra_count(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 12, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_string_pool_len(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 16, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_source_len(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 20, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_comment_count(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 24, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_diagnostic_count(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 28, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_program_index(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 32, 4); return v;
        \\}
        \\static inline uint32_t yuku_header_flags(const uint8_t* buf) {
        \\    uint32_t v; memcpy(&v, buf + 36, 4); return v;
        \\}
        \\
        \\// node accessors
        \\static inline const YukuPackedNode* yuku_node_ptr(const uint8_t* buf, uint32_t idx) {
        \\    return (const YukuPackedNode*)(buf + YUKU_HEADER_SIZE + (uint64_t)idx * YUKU_NODE_SIZE);
        \\}
        \\static inline YukuNodeType yuku_node_type(const uint8_t* buf, uint32_t idx) {
        \\    return (YukuNodeType)yuku_node_ptr(buf, idx)->tag;
        \\}
        \\static inline uint32_t yuku_node_span_start(const uint8_t* buf, uint32_t idx) {
        \\    return yuku_node_ptr(buf, idx)->span_start;
        \\}
        \\static inline uint32_t yuku_node_span_end(const uint8_t* buf, uint32_t idx) {
        \\    return yuku_node_ptr(buf, idx)->span_end;
        \\}
        \\
        \\// raw slot access (field1..field5 are u32 slots 0..4)
        \\static inline uint32_t yuku_slot(const uint8_t* buf, uint32_t idx, int slot) {
        \\    const YukuPackedNode* n = yuku_node_ptr(buf, idx);
        \\    switch (slot) {
        \\        case 0: return n->field1;
        \\        case 1: return n->field2;
        \\        case 2: return n->field3;
        \\        case 3: return n->field4;
        \\        case 4: return n->field5;
        \\        default: return YUKU_NULL_INDEX;
        \\    }
        \\}
        \\
        \\// flag access
        \\static inline bool yuku_flag(const uint8_t* buf, uint32_t idx, int bit) {
        \\    return (yuku_node_ptr(buf, idx)->flags >> bit) & 1;
        \\}
        \\static inline uint32_t yuku_flag_bits(const uint8_t* buf, uint32_t idx, int bit, int width) {
        \\    return (yuku_node_ptr(buf, idx)->flags >> bit) & ((1u << width) - 1);
        \\}
        \\
        \\// typed accessors
        \\
        \\// NodeIndex: returns YUKU_NULL_INDEX (0xFFFFFFFF) for null
        \\static inline uint32_t yuku_get_node(const uint8_t* buf, uint32_t idx, int slot) {
        \\    return yuku_slot(buf, idx, slot);
        \\}
        \\static inline bool yuku_is_null(uint32_t node_idx) {
        \\    return node_idx == YUKU_NULL_INDEX;
        \\}
        \\
        \\// IndexRange children
        \\static inline uint32_t yuku_children_len(const uint8_t* buf, uint32_t idx, int slot, bool is_first) {
        \\    if (is_first) return yuku_node_ptr(buf, idx)->field0;
        \\    return yuku_slot(buf, idx, slot + 1);
        \\}
        \\static inline uint32_t yuku_children_start(const uint8_t* buf, uint32_t idx, int slot) {
        \\    return yuku_slot(buf, idx, slot);
        \\}
        \\
        \\// extra array access — returns the node index at extra[start + i]
        \\static inline uint32_t yuku_extra(const uint8_t* buf, uint32_t start, uint32_t i) {
        \\    const uint8_t* extra_base = buf + YUKU_HEADER_SIZE
        \\        + (uint64_t)yuku_header_node_count(buf) * YUKU_NODE_SIZE;
        \\    uint32_t v; memcpy(&v, extra_base + (uint64_t)(start + i) * 4, 4);
        \\    return v;
        \\}
        \\
        \\// String: returns {start, end}. caller resolves to source text or pool.
        \\typedef struct { uint32_t start; uint32_t end; } YukuString;
        \\static inline YukuString yuku_get_string(const uint8_t* buf, uint32_t idx, int slot) {
        \\    YukuString s;
        \\    s.start = yuku_slot(buf, idx, slot);
        \\    s.end   = yuku_slot(buf, idx, slot + 1);
        \\    return s;
        \\}
        \\
        \\// resolve a YukuString to a pointer and length.
        \\// source: the original source pointer (from YukuResult.source).
        \\//         for pool strings, source is not used and can be NULL.
        \\static inline const char* yuku_str_ptr(const char* source, const uint8_t* buf, YukuString s, size_t* out_len) {
        \\    if (s.start == s.end) { *out_len = 0; return ""; }
        \\    uint32_t src_len = yuku_header_source_len(buf);
        \\    if (s.start < src_len) {
        \\        *out_len = s.end - s.start;
        \\        return source + s.start;
        \\    }
        \\    uint32_t pool_off = s.start - src_len;
        \\    *out_len = s.end - s.start;
        \\    const uint8_t* pool = buf + YUKU_HEADER_SIZE
        \\        + (uint64_t)yuku_header_node_count(buf) * YUKU_NODE_SIZE
        \\        + (uint64_t)yuku_header_extra_count(buf) * 4;
        \\    return (const char*)(pool + pool_off);
        \\}
        \\
        \\// string pool pointer (for direct pool access)
        \\static inline const uint8_t* yuku_string_pool(const uint8_t* buf) {
        \\    return buf + YUKU_HEADER_SIZE
        \\        + (uint64_t)yuku_header_node_count(buf) * YUKU_NODE_SIZE
        \\        + (uint64_t)yuku_header_extra_count(buf) * 4;
        \\}
        \\
        \\
    );
}

// ============================================================================
// per-node macros (the key generated part)
// ============================================================================

fn writePerNodeMacros(w: *Writer) !void {
    try w.writeAll("// --- per-node field macros ---\n");
    try w.writeAll("// usage: YUKU_FIELD(binary_expression, left)(buf, node_idx) -> uint32_t\n\n");
    try w.writeAll(
        \\// helper: resolve string pool offset
        \\#define YUKU_STR_POOL_OFF(buf, s) ((s).start < yuku_header_source_len(buf) ? 0 : (s).start - yuku_header_source_len(buf))
        \\
        \\
    );

    @setEvalBranchQuota(100_000);
    const union_fields = @typeInfo(ast.NodeData).@"union".fields;
    inline for (union_fields) |uf| {
        const T = uf.type;
        if (@typeInfo(T) != .@"struct") continue;
        if (@typeInfo(T).@"struct".fields.len == 0) continue;

        const struct_fields = @typeInfo(T).@"struct".fields;

        inline for (struct_fields, 0..) |sf, fi| {
            const FT = sf.type;

            if (FT == ast.NodeIndex) {
                const slot = comptime rt.u32SlotForField(T, fi);
                try w.print("#define YUKU_{s}_{s}(buf, idx) yuku_slot(buf, idx, {d})\n", .{ uf.name, sf.name, slot });
            } else if (FT == ast.IndexRange) {
                const slot = comptime rt.u32SlotForField(T, fi);
                if (comptime rt.isFirstRange(T, fi)) {
                    try w.print("#define YUKU_{s}_{s}_START(buf, idx) yuku_slot(buf, idx, {d})\n", .{ uf.name, sf.name, slot });
                    try w.print("#define YUKU_{s}_{s}_LEN(buf, idx) (yuku_node_ptr(buf, idx)->field0)\n", .{ uf.name, sf.name });
                } else {
                    try w.print("#define YUKU_{s}_{s}_START(buf, idx) yuku_slot(buf, idx, {d})\n", .{ uf.name, sf.name, slot });
                    try w.print("#define YUKU_{s}_{s}_LEN(buf, idx) yuku_slot(buf, idx, {d})\n", .{ uf.name, sf.name, slot + 1 });
                }
            } else if (FT == ast.String) {
                const slot = comptime rt.u32SlotForField(T, fi);
                try w.print("#define YUKU_{s}_{s}(buf, idx) yuku_get_string(buf, idx, {d})\n", .{ uf.name, sf.name, slot });
            } else if (FT == bool) {
                const bit = comptime rt.flagBitForField(T, fi);
                try w.print("#define YUKU_{s}_{s}(buf, idx) yuku_flag(buf, idx, {d})\n", .{ uf.name, sf.name, bit });
            } else if (comptime rt.isEnumType(FT)) {
                const bit = comptime rt.flagBitForField(T, fi);
                const width = comptime rt.enumBitWidth(FT);
                try w.print("#define YUKU_{s}_{s}(buf, idx) yuku_flag_bits(buf, idx, {d}, {d})\n", .{ uf.name, sf.name, bit, width });
            } else if (FT == ?ast.ImportPhase) {
                const bit = comptime rt.flagBitForField(T, fi);
                try w.print("#define YUKU_{s}_{s}_PRESENT(buf, idx) yuku_flag(buf, idx, {d})\n", .{ uf.name, sf.name, bit });
                try w.print("#define YUKU_{s}_{s}_VALUE(buf, idx) yuku_flag_bits(buf, idx, {d}, {d})\n", .{ uf.name, sf.name, bit + 1, 1 });
            } else if (FT == ?ast.Hashbang) {
                const bit = comptime rt.flagBitForField(T, fi);
                const slot = comptime rt.u32SlotForField(T, fi);
                try w.print("#define YUKU_{s}_{s}_PRESENT(buf, idx) yuku_flag(buf, idx, {d})\n", .{ uf.name, sf.name, bit });
                try w.print("#define YUKU_{s}_{s}_VALUE(buf, idx) yuku_get_string(buf, idx, {d})\n", .{ uf.name, sf.name, slot });
            } else {
                @compileError("unsupported field type: " ++ @typeName(FT) ++ " in " ++ uf.name);
            }
        }
        try w.writeByte('\n');
    }
}

