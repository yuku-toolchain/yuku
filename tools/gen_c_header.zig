fn writeParseDecls(w: *Writer) !void {
    try w.writeAll(
        \\// --- parse API (implemented in src/parser/capi/root.zig) ---
        \\
        \\typedef enum {
        \\    YUKU_SOURCE_SCRIPT = 0,
        \\    YUKU_SOURCE_MODULE = 1,
        \\} YukuParseSourceType;
        \\
        \\typedef enum {
        \\    YUKU_LANG_JS  = 0,
        \\    YUKU_LANG_TS  = 1,
        \\    YUKU_LANG_JSX = 2,
        \\    YUKU_LANG_TSX = 3,
        \\    YUKU_LANG_DTS = 4,
        \\} YukuParseLang;
        \\
        \\// parse source code into a binary AST buffer.
        \\// the returned buffer is heap-allocated; call yuku_free() when done.
        \\// note: source strings in the AST reference the original source pointer
        \\// by byte offset. keep the source buffer alive while reading the AST.
        \\YukuResult yuku_parse(const char* source, size_t source_len,
        \\                     YukuParseSourceType source_type, YukuParseLang lang);
        \\
        \\// free the buffer returned by yuku_parse().
        \\void yuku_free(YukuResult* result);
        \\
        \\
    );
}
