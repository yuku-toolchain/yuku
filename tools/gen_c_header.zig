fn writeCommentAccessors(w: *Writer) !void {
    try w.writeAll(
        \\// --- comment accessors ---
        \\
        \\static inline const uint8_t* yuku_comments_ptr(const uint8_t* buf) {
        \\    return buf + YUKU_HEADER_SIZE
        \\        + (uint64_t)yuku_header_node_count(buf) * YUKU_NODE_SIZE
        \\        + (uint64_t)yuku_header_extra_count(buf) * 4
        \\        + yuku_header_string_pool_len(buf);
        \\}
        \\
        \\static inline uint8_t yuku_comment_type(const uint8_t* buf, uint32_t i) {
        \\    return yuku_comments_ptr(buf)[i * YUKU_COMMENT_SIZE];
        \\}
        \\static inline uint32_t yuku_comment_start(const uint8_t* buf, uint32_t i) {
        \\    uint32_t v; memcpy(&v, yuku_comments_ptr(buf) + i * YUKU_COMMENT_SIZE + 4, 4); return v;
        \\}
        \\static inline uint32_t yuku_comment_end(const uint8_t* buf, uint32_t i) {
        \\    uint32_t v; memcpy(&v, yuku_comments_ptr(buf) + i * YUKU_COMMENT_SIZE + 8, 4); return v;
        \\}
        \\static inline YukuString yuku_comment_value(const uint8_t* buf, uint32_t i) {
        \\    YukuString s;
        \\    memcpy(&s.start, yuku_comments_ptr(buf) + i * YUKU_COMMENT_SIZE + 12, 4);
        \\    memcpy(&s.end,   yuku_comments_ptr(buf) + i * YUKU_COMMENT_SIZE + 16, 4);
        \\    return s;
        \\}
        \\
        \\
    );
}

// ============================================================================
// diagnostic accessors
// ============================================================================

fn writeDiagnosticAccessors(w: *Writer) !void {
    try w.writeAll(
        \\// --- diagnostic accessors (variable-length, sequential) ---
        \\
        \\typedef struct {
        \\    YukuSeverity severity;
        \\    uint32_t span_start;
        \\    uint32_t span_end;
        \\    const char* message;
        \\    uint32_t message_len;
        \\    const char* help;
        \\    uint32_t help_len;
        \\} YukuDiagnostic;
        \\
        \\static inline const uint8_t* yuku_diagnostics_ptr(const uint8_t* buf) {
        \\    return yuku_comments_ptr(buf)
        \\        + (uint64_t)yuku_header_comment_count(buf) * YUKU_COMMENT_SIZE;
        \\}
        \\
        \\// read the i-th diagnostic. stores message/help pointers into internal buffer.
        \\// returns the byte offset past this diagnostic (for iterating to the next).
        \\static inline size_t yuku_diagnostic(const uint8_t* buf, uint32_t i, YukuDiagnostic* out) {
        \\    const uint8_t* base = yuku_diagnostics_ptr(buf);
        \\    size_t pos = 0;
        \\    // skip preceding diagnostics
        \\    for (uint32_t j = 0; j < i; j++) {
        \\        uint8_t sev = base[pos]; pos += 1;
        \\        pos += 8; // span_start + span_end
        \\        uint32_t ml; memcpy(&ml, base + pos, 4); pos += 4 + ml;
        \\        uint8_t hh = base[pos]; pos += 1;
        \\        if (hh) { uint32_t hl; memcpy(&hl, base + pos, 4); pos += 4 + hl; }
        \\        uint32_t lc; memcpy(&lc, base + pos, 4); pos += 4;
        \\        for (uint32_t k = 0; k < lc; k++) { pos += 8; uint32_t lml; memcpy(&lml, base + pos, 4); pos += 4 + lml; }
        \\    }
        \\    // read the i-th diagnostic
        \\    out->severity = (YukuSeverity)base[pos]; pos += 1;
        \\    memcpy(&out->span_start, base + pos, 4); pos += 4;
        \\    memcpy(&out->span_end, base + pos, 4); pos += 4;
        \\    memcpy(&out->message_len, base + pos, 4); pos += 4;
        \\    out->message = (const char*)(base + pos); pos += out->message_len;
        \\    uint8_t hh = base[pos]; pos += 1;
        \\    if (hh) {
        \\        memcpy(&out->help_len, base + pos, 4); pos += 4;
        \\        out->help = (const char*)(base + pos); pos += out->help_len;
        \\    } else {
        \\        out->help = NULL; out->help_len = 0;
        \\    }
        \\    uint32_t lc; memcpy(&lc, base + pos, 4); pos += 4;
        \\    for (uint32_t k = 0; k < lc; k++) { pos += 8; uint32_t lml; memcpy(&lml, base + pos, 4); pos += 4 + lml; }
        \\    return pos;
        \\}
        \\
        \\
    );
}

// ============================================================================
