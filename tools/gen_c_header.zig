// generates yuku-c.h, a C header for reading the binary AST buffer produced by yuku_parse().
// uses the same comptime layout functions as the serializer (transfer.zig), guaranteeing
// the generated accessors match the binary format exactly.
//
// Usage:
//   zig build gen-c-header
//
// Output: zig-out/yuku-c.h
//
// Re-run whenever the AST definition (ast.zig) or binary format (transfer.zig) changes.
// The generated header is self-contained — no Zig dependencies at runtime.

const std = @import("std");
const parser = @import("parser");
const ast = parser.ast;
const rt = @import("transfer");

const Writer = std.Io.Writer;

pub fn main(init: std.process.Init) !void {
    const stdout = std.Io.File.stdout();
    var buf: [256 * 1024]u8 = undefined;
    var fw = stdout.writer(init.io, &buf);
    try generate(&fw.interface);
    try fw.flush();
}

fn generate(w: *Writer) !void {
    try w.writeAll(
        \\// generated by tools/gen_c_header.zig — do not edit
        \\#ifndef YUKU_C_H
        \\#define YUKU_C_H
        \\
        \\#include <stddef.h>
        \\#include <stdint.h>
        \\#include <stdbool.h>
        \\#include <string.h>
        \\
        \\#ifdef __cplusplus
        \\extern "C" {
        \\#endif
        \\
        \\// --- buffer layout constants ---
        \\
        \\#define YUKU_HEADER_SIZE  40
        \\#define YUKU_NODE_SIZE    32
        \\#define YUKU_COMMENT_SIZE 20
        \\#define YUKU_NULL_INDEX   0xFFFFFFFF
        \\
        \\// --- result struct (returned by yuku_parse) ---
        \\
        \\typedef struct {
        \\    uint8_t* buf;
        \\    size_t   size;
        \\    int      has_errors;
        \\    const char* source;
        \\} YukuResult;
        \\
        \\
    );
    try writeEnums(w);
    try writePackedNodeStruct(w);
    try writeInlineAccessors(w);
    try writePerNodeMacros(w);
    try writeCommentAccessors(w);
    try writeDiagnosticAccessors(w);
    try writeParseDecls(w);
    try w.writeAll(
        \\
        \\#ifdef __cplusplus
        \\}
        \\#endif
        \\
        \\#endif // YUKU_C_H
        \\
    );
}

